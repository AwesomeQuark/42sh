#include "libft.h"

size_t	ft_intsize(int n)
{
	size_t			size;

	if (n == -2147483648)
		return (11);
	size = 0;
	if (n < 0)
	{
		n = -n;
		size++;
	}
	while (n / 10)
	{
		n /= 10;
		size++;
	}
	size++;
	return (size);
}
