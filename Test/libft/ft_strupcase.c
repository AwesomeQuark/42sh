#include "libft.h"

char	*ft_strupcase(char *s)
{
	size_t i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'a' && s[i] <= 'z')
			s[i] -= 32;
		i++;
	}
	return (s);
}
